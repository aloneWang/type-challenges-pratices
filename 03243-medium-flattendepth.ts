type FlattenDepth<T extends unknown[], D extends number = 1, S extends unknown[] = []> = D extends S['length']
  ? T
  : T extends [infer L, ...infer R]
    ? L extends unknown[]
      ? [...FlattenDepth<L, D, [...S, 0]>, ...FlattenDepth<R, D, S>]
      : [L, ...FlattenDepth<R, D, S>]
    : []